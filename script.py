import re

def process_text(input_text):
    # Разделение текста на строки
    lines = input_text.strip().split('\n')

    # Обработка каждой строки
    sentences = []
    for line in lines:
        # Удаление чисел и лишних символов из строки
        sentence = re.sub(r'\b\d+(\.\d+)?\b', '', line)  # Удаление чисел
        sentence = sentence.strip(' ,\t\n')  # Удаление лишних пробелов, запятых и табуляций
        sentences.append(sentence)

    # Сохранение результатов в файл и вывод прогресса
    with open("output.txt", "w") as file:
        file.write("const q = [\n")
        for sentence in sentences:
            file.write(f'    "{sentence}",\n')
            print(f'Обработано: "{sentence}"')
        file.write("];\n")

    print("\nРезультат сохранён в файле 'output.txt'.")

